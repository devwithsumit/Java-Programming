package IF_Else;

public class Q12_GreatestInTwoNum {
    public static void main(String[] args) {
        int a = 22;
        double b = 22.999f;
        if(a == b){
            System.out.println("equal");
        } else if (a > b) {
            System.out.println("a: " + a);
        }else{
            System.out.println("b: "+ b);
        }
    }
}
